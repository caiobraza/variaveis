package variaveis;

public class Tipos_de_variaveis {

	public static void main(String[] args) {
		/* Tipos de variaveis -variavel, que é
		   simplesmente um espaço na memória
		   o qual reservamos e damos um nome.
		 */
		
		/* Variaveis do tipo
		 * inteiro
		 * real
		 * string
		 * lógico
		 */
		
		Double salarioDoFuncionario = 1300.50;
		Double bonusMensal = 250.00;
		Double horas = 2.0;
		
		Double saldoDaConta = (salarioDoFuncionario + bonusMensal) * horas;
		
		System.out.println("O valor do saldo é: " + saldoDaConta);
		
		
		
		
	
	}

}
